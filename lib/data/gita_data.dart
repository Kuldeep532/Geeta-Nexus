List<Verse> getAllVerses() => kChapters.expand((c) => c.verses).toList();

Verse getDailyVerse() {
  final allVerses = getAllVerses();
  final now = DateTime.now();
  final dayOfYear = now.difference(DateTime(now.year, 1, 1)).inDays;
  return allVerses[dayOfYear % allVerses.length];
}

List<Verse> searchVerses(String query) {
  final q = query.toLowerCase();
  return getAllVerses().where((v) {
    return v.translation.toLowerCase().contains(q) ||
        v.meaning.toLowerCase().contains(q) ||
        v.keywords.any((k) => k.toLowerCase().contains(q)) ||
        v.transliteration.toLowerCase().contains(q);
  }).toList();
}

